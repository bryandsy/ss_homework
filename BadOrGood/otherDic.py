def genDayhotDic(fn):
    d = {}
    with open(fn) as inf:
        for l in inf:
            ymd, cnts = l.strip().split(' ', 1)
            year, month, day = map(lambda _: int(_), ymd.split('-'))
            cntList = map(lambda _: int(_), cnts.split(' '))
            d[year] = d[year] if d.has_key(year) else {}
            d[year][month] = d[year][month] if d[year].has_key(month) else {}
            d[year][month][day] = d[year][month][day] if d[year][month].has_key(day) else {}
            for h in xrange(24): d[year][month][day][h] = cntList[h]
    return d

